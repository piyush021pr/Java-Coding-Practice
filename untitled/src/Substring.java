public class Substring {
    public static void main(String[] args) {
        String s1="Sachin Tendulkar";
        System.out.println("original String "+s1);
        System.out.println("Substring starting from 7th position "+s1.substring(7));
        System.out.println("Substring starting from index 0 to 6 "+s1.substring(0,6));
    }
}
